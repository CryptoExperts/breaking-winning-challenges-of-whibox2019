void store_memory(int ret_offset, int num_j) {
  memset(S, 0, 1024);

  for (int j = 0; j < num_j; ++j) {
    for (int i = 0; i < 14; i++) {
      for (int b = 0; b < 8; ++b) {
	int x = i*8+b;
	S[j*32 + i] ^=  ((memory[*((u32 *)(T + ret_offset) + x)] >> j) & 1) << (7 ^ b);
      }
    }
  }
}

