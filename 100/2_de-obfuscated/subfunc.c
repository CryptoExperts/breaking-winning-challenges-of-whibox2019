u8 hex2char (u8 * s) {
  u8 val = 0;

  if(*s>='0' && *s<='9') {
    val = *s - 48;
  } else {
    val = *s - 97 + 10;
  }
  val <<= 4;
  s++;
  if(*s>='0' && *s<='9') {
    val += *s - 48;
  } else {
    val += *s - 97 + 10;
  }
  return val;
}

int search(int arr[], int val, int arr_len) {
  for (int i = 0; i < arr_len; i++) {
    if (arr[i] == val) {
      return 1;
    }
  }
  return 0;
}

int binary_search(int arr[], int val, int arr_len) {
  int start = 0;
  int end = arr_len;
  while (start < end) {
    int mid = (start+end)/2;
    // printf("%d %d %d %d\n",start,end, mid, arr[mid]);
    if (arr[mid] > val) {
      end = mid;
    } else if (arr[mid] < val) {
      //printf("ss\n");
      if (start == mid) {
	return 0;
      } else {
	start = mid;
      }

    } else {
      return 1;
    }
  }

  return 0;
}

int parity(u64 v) {
  v ^= v >> 32 ;
  v ^= v >> 16 ;
  v ^= v >> 8 ;
  v ^= v >> 4 ;
  v &= 0xf;
  return (0x6996 >> v) & 1;
}

int ones(u64 x[], int len) {
  int c = 0;
  for (int i = 0; i < len; ++i) {
    for (int j = 0; j < 64; ++j)
      {
	c += (x[i] >> j) & 1;
      }
  }
  return c;
}

u64 byte_parity(u64 v) {
  u64 w = parity(v & 0xff);
  w += ((u64)parity(v & 0xff00))   << 8;
  w += ((u64)parity(v & 0xff0000)) << 8*2;
  w += ((u64)parity(v & 0xff000000)) << 8*3;
  w += ((u64)parity(v & 0xff00000000)) << 8*4;
  w += ((u64)parity(v & 0xff0000000000)) << 8*5;
  w += ((u64)parity(v & 0xff000000000000)) << 8*6;
  w += ((u64)parity(v & 0xff00000000000000)) << 8*7;
  return w;
}


