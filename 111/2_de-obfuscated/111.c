int hw(u32 x) {
  u32 v;

  v = (x & 0x55555555) + ((x & 0xaaaaaaaa) >> 1);
  v = (v & 0x33333333) + ((v & 0xcccccccc) >> 2);
  v = (v & 0x0f0f0f0f) + ((v & 0xf0f0f0f0) >> 4);
  v = (v & 0x00ff00ff) + ((v & 0xff00ff00) >> 8);
  v = (v & 0x0000ffff) + ((v & 0xffff0000) >> 16);

  return (int) v;
}

void loop_ssa(int ptr_offset, int steps, u32 * mem, u8 * op, u32 * operand1, u32 * operand2, u32 * mask1, u32 * mask2) {
  for (int i = ptr_offset; i < steps; ++i) {
    u32 op1 = operand1[i];
    u32 op2 = operand2[i];

    if (op[i] == 0) {
      mem[i] = mem[op1] ^ mem[op2];
    } else {
      mem[i] = (mem[op1] ^ mask1[i]) & (mem[op2] ^ mask2[i]);
    }
  }

}

void loop_ssa_mod(int ptr_offset, int steps, u32 * mem, u8 * op, u32 * operand1, u32 * operand2, u32 * mask1, u32 * mask2) {
  for (int i = ptr_offset; i < steps; ++i) {
    u32 op1 = operand1[i];
    u32 op2 = operand2[i];

    if (op[i] == 0) {
      mem[i] = mem[op1] ^ mem[op2];
    } else {
      mem[i] = (mem[op1] ^ mask1[i]) & (mem[op2] ^ mask2[i]);
    }
  }

}

void large_to_small(u32 * from, u8 * to, int N, int size) {
  memset(to, 0, 1024);
  for (int i = 0; i < N/8; i++) {
    for (int b = 0; b < 8; b++) {
      int v = from[i*8+b];
      for (int j = 0; j < size; ++j) {
	to[i + j*32] ^= (((v >> j) & 1) << (7 ^ b));
      }
    }
  }
}

void small_to_large(u8 * from, u32 * to, int N, int size) {
  memset(to, 0, sizeof(u32)*N);
  for (int i = 0; i < N/8; i++) {
    for (int j = 0 ; j < size; j++) {
      int v = from[i + j*32];
      for (int b = 0; b < 8; b++) {
	to[i*8+b] ^= ( (v >> (b^7) )  & 1) << j;
      }

    }
  }
}

